func TestCompile_BasicSpec(t *testing.T) {
	dir := t.TempDir()
	specFile := filepath.Join(dir, "app.spec.promptc")
	content := `features:
  - hello: "World"
`
	if err := os.WriteFile(specFile, []byte(content), 0644); err != nil {
		t.Fatal(err)
	}

	comp := NewCompiler(dir)
	outPath, err := comp.Compile(specFile, nil)
	if err != nil {
		t.Fatalf("Compile failed: %v", err)
	}
	if outPath == "" {
		t.Fatal("Compile returned empty output path")
	}

	data, err := os.ReadFile(outPath)
	if err != nil {
		t.Fatalf("failed to read output: %v", err)
	}
	out := string(data)
	if !strings.Contains(out, "Generated by promptc") {
		t.Error("output missing metadata header")
	}
	if !strings.Contains(out, "World") {
		t.Error("output missing feature content")
	}
}

func TestCompile_OutputDir(t *testing.T) {
	dir := t.TempDir()
	specFile := filepath.Join(dir, "myapp.spec.promptc")
	content := `features:
  - f: "test"
`
	if err := os.WriteFile(specFile, []byte(content), 0644); err != nil {
		t.Fatal(err)
	}

	outDir := filepath.Join(dir, "custom-output")
	comp := NewCompiler(dir)
	outPath, err := comp.Compile(specFile, &CompileOptions{OutputDir: outDir})
	if err != nil {
		t.Fatalf("Compile failed: %v", err)
	}

	expected := filepath.Join(outDir, "instructions.promptc")
	if outPath != expected {
		t.Errorf("output path = %q, want %q", outPath, expected)
	}
	if _, err := os.Stat(expected); os.IsNotExist(err) {
		t.Error("output file was not created")
	}
}

