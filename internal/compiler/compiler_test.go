func TestParseSpec_Resources(t *testing.T) {
	dir := t.TempDir()
	specFile := filepath.Join(dir, "res.spec.promptc")
	content := `features:
  - f: "test"
resources:
  - url: "https://example.com/docs"
    name: example-docs
  - git: "https://github.com/org/repo"
    name: ref-repo
    ref: main
    path: docs/
`
	if err := os.WriteFile(specFile, []byte(content), 0644); err != nil {
		t.Fatal(err)
	}

	comp := NewCompiler(dir)
	spec, err := comp.ParseSpec(specFile)
	if err != nil {
		t.Fatalf("ParseSpec failed: %v", err)
	}

	if len(spec.Resources) != 2 {
		t.Fatalf("expected 2 resources, got %d", len(spec.Resources))
	}
	if spec.Resources[0].URL != "https://example.com/docs" {
		t.Errorf("wrong URL: %q", spec.Resources[0].URL)
	}
	if spec.Resources[1].Git != "https://github.com/org/repo" {
		t.Errorf("wrong Git: %q", spec.Resources[1].Git)
	}
	if spec.Resources[1].Ref != "main" {
		t.Errorf("wrong Ref: %q", spec.Resources[1].Ref)
	}
}

