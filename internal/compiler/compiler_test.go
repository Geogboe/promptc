func TestCompile_WithImports(t *testing.T) {
	dir := t.TempDir()

	promptsDir := filepath.Join(dir, "prompts")
	if err := os.MkdirAll(promptsDir, 0755); err != nil {
		t.Fatal(err)
	}
	libContent := "# My Library\nSome guidelines here."
	if err := os.WriteFile(filepath.Join(promptsDir, "mylib.prompt"), []byte(libContent), 0644); err != nil {
		t.Fatal(err)
	}

	specFile := filepath.Join(dir, "app.spec.promptc")
	content := `imports:
  - mylib
features:
  - f: "test"
`
	if err := os.WriteFile(specFile, []byte(content), 0644); err != nil {
		t.Fatal(err)
	}

	comp := NewCompiler(dir)
	outPath, err := comp.Compile(specFile, nil)
	if err != nil {
		t.Fatalf("Compile failed: %v", err)
	}

	data, _ := os.ReadFile(outPath)
	if !strings.Contains(string(data), "My Library") {
		t.Error("output missing imported library content")
	}
}

